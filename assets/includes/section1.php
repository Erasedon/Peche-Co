            <h1>Qui sommes-nous ?</h1>
            <p>La pêche est une des activités <b>les plus populaires</b> et <b>traditionnelles</b> car elle répond à de nombreuses aspirations individuelles, <b>collectives et écologiques</b>. Sa réglementation <b>minutieuse</b> contribue à préserver un <b>milieu naturel français diversifié</b>, riche et propice à une biodiversité aquatique irremplaçable.

            Pour pouvoir pêcher, vous devez disposer d’une carte de pêche. Cette carte matérialise votre appartenance à une association de pêche et de protection du milieu aquatique (AAPPMA) et plus globalement votre intégration au réseau associatif de la pêche de loisir. <br><br>Par cette adhésion, vous contribuez également au financement de plusieurs actions en <b>faveur de la protection et la préservation de notre milieu naturel et de sa faune piscicole</b>. Vous pouvez vous investir dans les activités des associations de pêche pour la transmission du <b>loisir, l’entretien et la surveillance des milieux aquatiques</b>.

            La pratique de la pêche implique, sous la responsabilité de chacun, l’adhésion aux valeurs véhiculées par les structures associatives de la pêche de loisir dont le respect de la sécurité et de la réglementation, du milieu naturel, des espèces pêchées, des lieux de pêche et des autres usagers (pêcheurs et autres). </p>
